class Circle {
  constructor(radius) {
    this.radius = radius
  }
  
  set diameter(diameter) {
    this.diameter = diameter
  }
  
  get diameter() {
    return this.diameter
  }
  
  set circumference(circumference) {
    this.circumference = circumference
  }
  
  get circumference() {
    return circumference
  }
  
  set area(area) {
    this.area = area
  }
  
  get area() {
    return area
  }
  
  
}